#!/usr/bin/env -S uv run
# /// script
# requires-python = ">=3.11"
# dependencies = []
# ///
from __future__ import annotations

import argparse
import shlex
import subprocess
from pathlib import Path
import os


SKILL_DIR = Path(__file__).resolve().parent.parent
SCRIPTS_DIR = SKILL_DIR / "scripts"
DEFAULT_GPT_IMAGE_EDIT = (
    SKILL_DIR.parent / "gpt-image-2-0" / "scripts" / "gpt_image_edit.py"
)


def run(cmd: list[str]) -> None:
    print("$", " ".join(shlex.quote(part) for part in cmd))
    subprocess.run(cmd, check=True)


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(
        description="Run the animated spritesheet salvage pipeline from generation or from an existing sheet."
    )
    parser.add_argument("--work-dir", type=Path, required=True, help="Output working directory for this run.")

    generation = parser.add_argument_group("Generation inputs")
    generation.add_argument("--reference", type=Path, help="Primary reference image for generation.")
    generation.add_argument("--guide", type=Path, help="Sheet guide image, e.g. alternating-pixel 512x1280 PNG.")
    generation.add_argument("--prompt-file", type=Path, help="Prompt file for gpt-image-2 generation.")
    generation.add_argument("--sheet-size", default="512x1280", help="Generation size, default 512x1280.")
    generation.add_argument("--sheet-prefix", default="generated-sheet", help="Output filename prefix for generated sheet.")
    generation.add_argument(
        "--skip-generation-sheet",
        type=Path,
        default=None,
        help="Use an existing generated sheet instead of calling gpt-image-2.",
    )
    generation.add_argument("--quality", default="high", choices=["low", "medium", "high", "auto"])
    generation.add_argument("--background", default="opaque", choices=["opaque", "auto"])
    generation.add_argument(
        "--gpt-image-edit-script",
        type=Path,
        default=None,
        help="Optional path to the gpt-image-2 edit helper. Defaults to the sibling gpt-image-2-0 skill.",
    )

    layout = parser.add_argument_group("Layout")
    layout.add_argument("--rows", type=int, required=True)
    layout.add_argument("--cols", type=int, required=True)
    layout.add_argument("--frame-canvas", default="256x256")
    layout.add_argument("--center-x", type=int, default=128)
    layout.add_argument("--bottom-y", type=int, default=255)
    layout.add_argument("--component-threshold", type=int, default=15)

    bg = parser.add_argument_group("Background removal")
    bg.add_argument("--remove-bg", action="store_true", help="Run remove.bg on recovered component crops.")
    bg.add_argument("--remove-bg-api-key-env", default="REMOVE_BG_API_KEY")

    review = parser.add_argument_group("Review")
    review.add_argument("--selected-order", default="", help="Comma-separated selected frame order, e.g. 01,03,02,04,05")
    review.add_argument("--durations-ms", default="", help="Optional comma-separated durations matching selected-order.")
    review.add_argument("--flat-bg", default="#f0f0f0", help="Flat background for contact sheet and GIF previews.")
    review.add_argument("--label-prefix", default="F", help="Prefix for autogenerated labels, default F.")

    return parser.parse_args()


def require_generation_inputs(args: argparse.Namespace) -> None:
    if args.skip_generation_sheet is not None:
        return
    missing = [name for name in ["reference", "guide", "prompt_file"] if getattr(args, name) is None]
    if missing:
        raise SystemExit(f"Missing generation inputs: {', '.join(missing)}")


def main() -> None:
    args = parse_args()
    require_generation_inputs(args)

    work_dir = args.work_dir.resolve()
    work_dir.mkdir(parents=True, exist_ok=True)
    generated_dir = work_dir / "generated"
    recovered_dir = work_dir / "recovered-components"
    matte_dir = work_dir / "recovered-components-no-bg"
    normalized_dir = work_dir / "normalized-frames"
    review_dir = work_dir / "review"
    review_dir.mkdir(parents=True, exist_ok=True)
    generated_dir.mkdir(parents=True, exist_ok=True)

    if args.skip_generation_sheet is not None:
        sheet_path = args.skip_generation_sheet.resolve()
    else:
        gpt_image_edit = (
            args.gpt_image_edit_script
            or (
                Path(os.environ["ANIMATED_SPRITESHEETS_GPT_IMAGE_EDIT"])
                if "ANIMATED_SPRITESHEETS_GPT_IMAGE_EDIT" in os.environ
                else DEFAULT_GPT_IMAGE_EDIT
            )
        ).resolve()
        if not gpt_image_edit.exists():
            raise SystemExit(
                "gpt-image-2 edit script not found. "
                f"Tried: {gpt_image_edit}. "
                "Pass --gpt-image-edit-script or set ANIMATED_SPRITESHEETS_GPT_IMAGE_EDIT."
            )
        prompt_text = args.prompt_file.read_text()
        run(
            [
                "python3",
                str(gpt_image_edit),
                "--image",
                str(args.reference.resolve()),
                "--image",
                str(args.guide.resolve()),
                "--prompt",
                prompt_text,
                "--out-dir",
                str(generated_dir),
                "--filename-prefix",
                args.sheet_prefix,
                "--size",
                args.sheet_size,
                "--quality",
                args.quality,
                "--output-format",
                "png",
                "--background",
                args.background,
            ]
        )
        generated = sorted(generated_dir.glob(f"{args.sheet_prefix}-*.png"))
        if not generated:
            raise SystemExit("Generation step completed without writing a sheet PNG.")
        sheet_path = generated[0]

    run(
        [
            "uv",
            "run",
            str(SCRIPTS_DIR / "recover_component_frames.py"),
            str(sheet_path),
            "--rows",
            str(args.rows),
            "--cols",
            str(args.cols),
            "--threshold",
            str(args.component_threshold),
            "--out-dir",
            str(recovered_dir),
            "--prefix",
            "frame",
        ]
    )

    recovery_source = recovered_dir
    recovery_glob = "frame-*.png"
    if args.remove_bg:
        run(
            [
                "uv",
                "run",
                str(SCRIPTS_DIR / "remove_bg_batch.py"),
                "--input-dir",
                str(recovered_dir),
                "--out-dir",
                str(matte_dir),
                "--glob",
                recovery_glob,
                "--api-key-env",
                args.remove_bg_api_key_env,
            ]
        )
        recovery_source = matte_dir
        recovery_glob = "frame-*-no-bg.png"

    run(
        [
            "uv",
            "run",
            str(SCRIPTS_DIR / "normalize_frames.py"),
            "--input-dir",
            str(recovery_source),
            "--out-dir",
            str(normalized_dir),
            "--glob",
            recovery_glob,
            "--canvas",
            args.frame_canvas,
            "--center-x",
            str(args.center_x),
            "--bottom-y",
            str(args.bottom_y),
            "--flat-bg",
            args.flat_bg,
        ]
    )

    frame_files = sorted(normalized_dir.glob(recovery_glob))
    labels = [f"{args.label_prefix}{index:02d}" for index in range(1, len(frame_files) + 1)]
    labels_json = review_dir / "labels.json"
    labels_json.write_text(__import__("json").dumps(labels))

    run(
        [
            "uv",
            "run",
            str(SCRIPTS_DIR / "build_contact_sheet.py"),
            "--input-dir",
            str(normalized_dir),
            "--glob",
            recovery_glob,
            "--rows",
            str(args.rows),
            "--cols",
            str(args.cols),
            "--labels-json",
            str(labels_json),
            "--out",
            str(review_dir / "contact-sheet.png"),
            "--bg",
            args.flat_bg,
        ]
    )

    if args.selected_order:
        run(
            [
                "uv",
                "run",
                str(SCRIPTS_DIR / "build_sequence_gif.py"),
                "--input-dir",
                str(normalized_dir),
                "--pattern",
                recovery_glob.replace("*", "{id}"),
                "--order",
                args.selected_order,
                "--out",
                str(review_dir / "selected-sequence.gif"),
                "--flat-bg",
                args.flat_bg,
            ]
            + (
                ["--durations-ms", args.durations_ms]
                if args.durations_ms
                else []
            )
        )

    print("Pipeline complete.")
    print(f"Sheet: {sheet_path}")
    print(f"Recovered: {recovered_dir}")
    print(f"Normalized: {normalized_dir}")
    print(f"Review: {review_dir}")


if __name__ == "__main__":
    main()
